const fs = require('fs');

console.log('=== CSV → JSON konverzió ===\n');

// CSV fájl beolvasása
const csvPath = './db/t_onkorm_tech_20251006.csv';
const csvContent = fs.readFileSync(csvPath, 'utf8');

console.log(`CSV fájl beolvasva: ${csvPath}`);
console.log(`CSV méret: ${csvContent.length} karakter\n`);

// CSV feldolgozás
const lines = csvContent.split('\n').filter(line => line.trim());
const jsonArray = [];

// Header sor átugrása (első sor)
for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    // Parse CSV line: "ksh";"onev"
    // Remove quotes and split by semicolon
    const parts = line.split(';').map(part => part.replace(/^"|"$/g, '').trim());

    if (parts.length >= 2) {
        jsonArray.push({
            ksh: parts[0],
            onev: parts[1]
        });
    }
}

console.log(`Feldolgozott rekordok száma: ${jsonArray.length}`);

// JSON generálás (minimalizált formátum)
const jsonString = JSON.stringify(jsonArray);
console.log(`JSON méret: ${jsonString.length} karakter (${Math.round((jsonString.length / csvContent.length) * 100)}% a CSV-hez képest)\n`);

// data.js fájl beolvasása
let dataJsContent = fs.readFileSync('./js/data.js', 'utf8');

// Keressük meg a beágyazandó részt és cseréljük le
const startMarker = 'const EMBEDDED_JSON_DATA = ';
const endMarker = ';';

let startPos = dataJsContent.indexOf(startMarker);

// Ha még CSV formátum van, akkor frissítsük a template-et
if (startPos === -1) {
    console.log('Régi CSV formátum detektálva, átalakítás JSON-ra...');

    // Új data.js content generálása
    dataJsContent = `// Embedded JSON Data
// This file is auto-generated by convert-csv-to-json.js

const EMBEDDED_JSON_DATA = ${jsonString};
`;
} else {
    // JSON formátum már létezik, csak frissítjük
    const actualStart = startPos + startMarker.length;
    const endPos = dataJsContent.indexOf(endMarker, actualStart);

    if (endPos === -1) {
        console.error("Hiba: Nem található a záró marker!");
        process.exit(1);
    }

    // Összeállítjuk az új data.js tartalmat
    dataJsContent = (
        dataJsContent.substring(0, actualStart) +
        jsonString +
        dataJsContent.substring(endPos)
    );
}

// Kiírjuk az új data.js fájlt
fs.writeFileSync('./js/data.js', dataJsContent, 'utf8');

console.log('✓ js/data.js fájl frissítve');
console.log(`\nSikeres konverzió!`);
console.log(`Méretcsökkentés: ${csvContent.length - jsonString.length} karakter (${Math.round(((csvContent.length - jsonString.length) / csvContent.length) * 100)}%)`);
